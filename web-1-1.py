import sys
import pprint
from io import BytesIO
from find_par import find_params
# Этот класс поможет нам сделать картинку из потока байт

import requests
from PIL import Image

# Пусть наше приложение предполагает запуск:
# python search.py Москва, ул. Ак. Королева, 12
# Тогда запрос к геокодеру формируется следующим образом:
toponym_to_find = " ".join(sys.argv[1:])

geocoder_api_server = "http://geocode-maps.yandex.ru/1.x/"

geocoder_params = {
    "apikey": "40d1649f-0493-4b70-98ba-98533de7710b",
    "geocode": toponym_to_find,
    "format": "json"}

response = requests.get(geocoder_api_server, params=geocoder_params)

if not response:
    # обработка ошибочной ситуации
    pass

# Преобразуем ответ в json-объект
json_response = response.json()
pp = pprint.PrettyPrinter(indent=2)
pp.pprint(json_response)
# Получаем первый топоним из ответа геокодера.
toponym = json_response["response"]["GeoObjectCollection"][
    "featureMember"][0]["GeoObject"]
# Координаты центра топонима:
toponym_coodrinates = toponym["Point"]["pos"]
# Долгота и широта:
toponym_longitude, toponym_lattitude = toponym_coodrinates.split(" ")

delta1 = str(float(str(toponym["boundedBy"]["Envelope"]["upperCorner"]).split()[0]) - float(str(toponym["boundedBy"]["Envelope"]["lowerCorner"]).split()[0]))
delta2 = str(float(str(toponym["boundedBy"]["Envelope"]["upperCorner"]).split()[1]) - float(str(toponym["boundedBy"]["Envelope"]["lowerCorner"]).split()[1]))

params = find_params(toponym_longitude, toponym_lattitude, delta1, delta2)

map_api_server = "http://static-maps.yandex.ru/1.x/"
# ... и выполняем запрос
response = requests.get(map_api_server, params=params)

Image.open(BytesIO(
    response.content)).show()
# Создадим картинку
# и тут же ее покажем встроенным просмотрщиком операционной системы